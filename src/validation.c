static xmlSchema *INDI_SCHEMA = NULL;

/*--------------------------------------------------------------------------------------------------------------------*/

bool indi_validation_finalize()
{
    if(INDI_SCHEMA != NULL)
    {
        xmlSchemaFree(INDI_SCHEMA);

        INDI_SCHEMA = NULL;
    }

    return true;
}

/*--------------------------------------------------------------------------------------------------------------------*/

