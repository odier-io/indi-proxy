#include <stdio.h>

#include "indi_proxy_internal.h"

/*--------------------------------------------------------------------------------------------------------------------*/

static void fake_emit(str_t message)
{
    printf("%s\n", message);

    indi_free(message);
}

/*--------------------------------------------------------------------------------------------------------------------*/

bool indi_proxy_initialize(indi_proxy_t *proxy)
{
    /*----------------------------------------------------------------------------------------------------------------*/

    if(indi_validation_initialize() == false)
    {
        return false;
    }

    /*----------------------------------------------------------------------------------------------------------------*/

    proxy->emit = fake_emit;

    /*----------------------------------------------------------------------------------------------------------------*/

    return true;
}

/*--------------------------------------------------------------------------------------------------------------------*/

bool indi_proxy_finalize(indi_proxy_t *proxy)
{
    /*----------------------------------------------------------------------------------------------------------------*/

    proxy->emit = fake_emit;

    /*----------------------------------------------------------------------------------------------------------------*/

    return true;
}

